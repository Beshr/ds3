public class ClientObj {

	private String address;
	private String type;
	private int id;

	public ClientObj(String address, String type, int id) {
		this.address = address;
		this.type = type;
		this.id = id;
	}

	public String getAddress() {
		return address;
	}

	public String getType() {
		return type;
	}

	public int getId() {
		return id;
	}
	
	

}
